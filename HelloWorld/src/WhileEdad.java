
public class WhileEdad {

	public static void main(String[] args) {
		int edad = 1;
		while (edad < 18) {
			System.out.printf("%d", edad).println();
			edad++;
		}
		System.out.printf("Programa finalizado --> edad: %d", edad).println();

		int i = 25;
		while (i != 0) {
			i--;
			//System.out.println(i);
		}
		System.out.println(i);
	}

}
